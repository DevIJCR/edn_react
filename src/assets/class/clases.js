export class LinkSubMenu {
    #ref;
    #name;
    constructor(ref, name) {
        this.ref;
        this.name;
    }

    getRef(){
        return this.ref;
    }

    getName(){
        return this.name;
    }

    setRef(ref){
        this.ref = ref;
    }

    setName(name){
        this.name = name;
    }
}